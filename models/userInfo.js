const mongoose = require("mongoose");

const userSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    validate: {
      validator: function (v) {
        return /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/.test(v);
      },
      message: "Please enter a valid email",
    },
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const UserCollection = mongoose.model("User", userSchema, "UserCollection");
module.exports = UserCollection;
